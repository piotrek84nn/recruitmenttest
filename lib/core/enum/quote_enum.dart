enum Quote { usdt, usdc }

final quoteValues = EnumValues({"USDC": Quote.usdt, "USDT": Quote.usdc});

class EnumValues<T> {
  Map<String, T> map;
  Map<T, String>? reverseMap;

  EnumValues(this.map);

  Map<T, String>? get reverse {
    reverseMap ??= map.map((k, v) => MapEntry(v, k));
    return reverseMap;
  }
}
